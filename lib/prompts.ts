/** mode が不正・未指定のときは通常モードにフォールバック */
export function resolveGenerationMode(mode: unknown): GenerationMode {
  return typeof mode === "string" &&
    GENERATION_MODES.includes(mode as GenerationMode)
    ? (mode as GenerationMode)
    : "normal";
}

